// Generate Ozon product URL from SKU
export const getOzonProductUrl = (sku: string): string => {
  return `https://ozon.ru/product/${sku}`;
};

// Get Ozon product URL directly from product ID
export const getOzonProductUrlById = (productId: string): string | null => {
  const sku = getProductSku(productId);
  if (!sku) return null;
  return getOzonProductUrl(sku);
}; 